public class CicloWhile {
    public static void main(String[]args){
        
        int points = -30; 
        int seconds = 0;
        
        while(seconds <= 30){
            points++;
            seconds++;
            System.out.println("tiene: " + points + " puntos");
            System.out.println("han pasado " + seconds + "segundos");
            
            
        
       }
    }
    
}
